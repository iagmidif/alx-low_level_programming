#include "3-calc.h"

/**
 * op_add - adds two numbers
 * @a: first int
 * @b: second int
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts two numbers
 * @a: first int
 * @b: second int
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - muliplies two numbers
 * @a: first int
 * @b: second int
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: numerator
 * @b: denominator
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - gives the reaminder of the division of two numbers
 * @a: numerator
 * @b: denominator
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
