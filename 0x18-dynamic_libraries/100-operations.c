/**
 * add - adds two numbers
 * @a: first int
 * @b: second integer
 *
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: first int
 * @b: second integer
 *
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: first int
 * @b: second integer
 *
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: first int
 * @b: second integer
 *
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulous of two numbers
 * @a: first int
 * @b: second integer
 *
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
